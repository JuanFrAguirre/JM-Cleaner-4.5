//funcion para agregar los marcadores
function agregarMarcadores(arrayDeLocales, color) {
  for (let i = 0; i < arrayDeLocales.length; i++) {
    if (arrayDeLocales[i].coordenates) {
      const X_COORD = parseFloat(arrayDeLocales[i].coordenates.slice(0, 10));
      const Y_COORD = parseFloat(arrayDeLocales[i].coordenates.slice(-11, -1));
      // console.log(
      //   `las coordenadas del local ${arrayDeLocales[i].id} son: ${X_COORD} ${Y_COORD}`
      // );

      switch (color) {
        case "blue":
          L.marker([X_COORD, Y_COORD]).addTo(map);
          break;

        case "green":
          L.marker([X_COORD, Y_COORD], { icon: greenIcon }).addTo(map);
          break;

        default:
          break;
      }
    }
  }
}

//funciones especificas para el marcador segun el color
function agregarMarcadoresAzules(arrayDeLocales) {
  agregarMarcadores(arrayDeLocales, "blue");
}

function agregarMarcadoresVerdes(arrayDeLocales) {
  agregarMarcadores(arrayDeLocales, "green");
}

